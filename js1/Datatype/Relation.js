// Relation operator in javascript

// > greater than
// < less than
// >= greater than or eqaul to
// <= less than or eqaul to

let a = 10;

let b = 20;

console.log("Greater :", a>b);  // false
console.log("Lesser :", a<b);  // true
console.log("Greater than equal :", a>=b);  // false
console.log("Lesser  than equal :", a<=b);  // true